    public const string GuestSteps = """
开始前

1. 你需要已安装 MCLink P2P 测试器，并能正常启动 Minecraft Java。
2. 游戏版本、加载器和模组包要和房主完全一致。
3. 不需要开局域网世界；只有房主需要这样做。

交换连接码

1. 点击主界面的“加入联机”。
2. 把房主发来的完整“邀请码”粘贴到大输入框；不要只复制其中一部分。
3. 点击“生成回应码”。
4. 点击“复制回应码”，把完整内容发回给房主。
5. 保持 MCLink 开着，等房主粘贴回应码并点击“开始连接”。

进入 Minecraft

1. 当 MCLink 显示“连接成功”时，会出现类似 127.0.0.1:12345 的地址。
2. 点击“复制地址”。
3. 打开 Minecraft，进入“多人游戏”，选择“直接连接”。
4. 粘贴刚才的 127.0.0.1:端口 地址后连接。
5. 不要输入房主的真实内网 IP、公网 IP 或路由器地址；只使用 MCLink 显示的 127.0.0.1:端口。

游玩期间

1. 不要关闭 MCLink；关闭后 Minecraft 连接会断开。
2. 如果断线，双方都点“停止联机”，重新交换新的邀请码和回应码。
""";

    public const string TroubleshootingSteps = """
“没有找到世界”

只在房主电脑上处理：回到 Minecraft，确认已经进入单人存档并完成“对局域网开放”。重新查看聊天栏的端口，填入这个端口后再生成邀请码。

“连接码不可用”或“还缺少回应码”

连接码可能复制不完整、被改动或已过期。双方点“停止联机”，由房主重新生成邀请码，再重新生成回应码；发送时要复制完整内容。

“90 秒内没有连上”

这表示双方网络无法完成无中继直连，通常是运营商 NAT 或校园/公司网络限制造成的。先让双方停止联机并重新交换一次连接码；如果仍然失败，当前网络组合不能使用 MCLink 直连，需要换网络或改用带中继的联机方案。

“直连已断开”

检查网络是否刚切换、VPN 是否改变了网络，或任一方是否关闭了 MCLink。恢复网络后，双方停止联机并重新交换连接码。

Minecraft 显示版本或模组不兼容

这不是 MCLink 的问题。双方需要使用相同 Minecraft 版本、同一加载器，以及相同的模组和配置；再用 MCLink 显示的地址直接连接。
""";
